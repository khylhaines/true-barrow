export function getMysteryById(id) {
  return MYSTERIES[Number(id)] || null;
}

export function getAllMysteries() {
  return Object.entries(MYSTERIES).map(([id, value]) => ({
    id: Number(id),
    ...value,
  }));
}

export function getRandomMystery(excludeIds = []) {
  const exclude = new Set((excludeIds || []).map(Number));
  const pool = getAllMysteries().filter((m) => !exclude.has(m.id));
  if (!pool.length) return null;
  return pool[Math.floor(Math.random() * pool.length)];
}
